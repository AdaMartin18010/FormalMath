$moved = 0
$notFound = 0

foreach ($file in $files) {
    if (Test-Path $file) {
        Move-Item -Path $file -Destination $archiveDir -Force
        $moved++
        Write-Host "已移动: $file"
    } else {
        $notFound++
        Write-Host "未找到: $file"
    }
}

Write-Host "`n归档完成:"
Write-Host "  移动文件数: $moved"
Write-Host "  未找到文件数: $notFound"
